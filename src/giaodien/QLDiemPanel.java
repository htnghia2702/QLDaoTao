    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        //b1. kiem tra hop le du lieu
        String loi ="";
        if(txtTiengAnh.getText().length()==0)
            loi+="Vui long nhap diem Tieng Anh \n";
        if(txtTinHoc.getText().length()==0)
            loi+="Vui long nhap diem Tin Hoc \n";
        if(txtGDTC.getText().length()==0)
            loi+="Vui long nhap diem GDTC";       
        else{
            try {
                float tienganh = Float.parseFloat(txtTiengAnh.getText());
                float tinhoc = Float.parseFloat(txtTinHoc.getText());
                float gdtc = Float.parseFloat(txtGDTC.getText());
                if(tienganh < 0 || tienganh >10)               
                    loi += "Diem Tieng anh phai trong khoang 0 -- > 10\n";                    
                if(tinhoc < 0 || tinhoc >10)              
                    loi += "Diem Tin Hoc phai trong khoang 0 -- > 10\n";                                              
                if(gdtc < 0 || gdtc >10)             
                    loi += "Diem GDTC phai trong khoang 0 -- > 10\n";             
            } catch (Exception e) {
                loi +="Diem co kieu la cac so thuc\n";              
            }           
        }
        if(loi.length()>0)
            JOptionPane.showMessageDialog(this, loi);
        else{
            BangDiem bd = new BangDiem();
            bd.setMaSV(txtMaSV.getText());
            bd.setTiengAnh(Float.parseFloat(txtTiengAnh.getText()));
            bd.setTinHoc(Float.parseFloat(txtTinHoc.getText()));
            bd.setGDTC(Float.parseFloat(txtGDTC.getText()));
            if(BangDiemDAO.ThemMoiBD(bd) > 0){
                JOptionPane.showMessageDialog(this, "Them moi thanh cong !");
                Gan_Du_Lieu();
            }         
            else
                JOptionPane.showMessageDialog(this, "Them moi that bai");
            
        }   
    }//GEN-LAST:event_btnLuuActionPerformed

