    private void Gan_Du_Lieu() {
        BangDiemDAO dao = new BangDiemDAO();
        dsBD = dao.DocDuLieuBD();
        GanDuLieuTbl(dsBD);
    }
     private void GanDuLieuTbl(ArrayList<BangDiem> dsbd) {
        SinhVienDAO svDao = new SinhVienDAO();
        DefaultTableModel model = (DefaultTableModel)tblDB.getModel();
        model.setRowCount(0);
        for(BangDiem x:dsbd){
            SinhVien sv = svDao.TimSV(x.getMaSV());
            model.addRow( new Object[]{x.getMaSV(),sv.getHoTen(),x.getTiengAnh(),x.getTinHoc(),x.getGDTC()});
        }          
        tblDB.setModel(model);
    }
     private void GanDuLieutext(BangDiem bd) {
        txtMaSV.setText(bd.getMaSV());
        SinhVienDAO svDao = new SinhVienDAO();
        SinhVien sv = svDao.TimSV(bd.getMaSV());
        txtHoTen.setText(sv.getHoTen());
        txtTiengAnh.setText(String.valueOf(bd.getTiengAnh()));
        txtTinHoc.setText(String.valueOf(bd.getTinHoc()));
        txtGDTC.setText(String.valueOf(bd.getGDTC()));
        lblTB.setText(String.valueOf((bd.getTiengAnh()+bd.getTinHoc()+bd.getGDTC())/3 ));
    }
