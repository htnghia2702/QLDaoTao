    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
         SinhVienDAO dao = new SinhVienDAO();
        //b1. kiem tra hop le du lieu
        String maSV = txtMasv.getText();
        String hoTen = txtHoten.getText();
        boolean gioiTinh = rdNam.isSelected()==true?true:false;
        String email = txtEmail.getText();
        String dienThoai = txtSDT.getText();
        String diaChi = txtDiaChi.getText();
        String loi ="";
        if(maSV.length()==0)
            loi+="MaSV rong \n";        
        if(hoTen.length()==0)
            loi+="HoTen rong \n";
        if(email.length()==0)
            loi+="Email rong \n";
        if(dienThoai.length()==0)
            loi+= "DienThoai rong \n";
        else{
            try {
                int SDT = Integer.parseInt(dienThoai);
                if(dienThoai.length() != 10)
                    loi+= "SDT chi o 10 so";              
            } catch (Exception e) {
                loi+="DienThoai co kieu la so nguyen \n";
            }
        }
        if(diaChi.length()==0)
            loi+="DiaChi rong \n";
        
        if(loi.length()>0){
            JOptionPane.showMessageDialog(this, loi);
            return;
        }else{
           
            SinhVien sv = new SinhVien(maSV, hoTen, gioiTinh, email, dienThoai, diaChi);
            
            if(dao.ThemMoiSV(sv)>0){
                JOptionPane.showMessageDialog(this, "Them moi thanh cong");               
                
                GanDuLieu();
            }
            else{
                JOptionPane.showMessageDialog(this, "Them moi that bai");
            }
        }
        
    }//GEN-LAST:event_btnLuuActionPerformed

