    private void GanDuLieu() {
        SinhVienDAO dao = new SinhVienDAO();
        dsSV = dao.DocDuLieuSV();
        GanDuLieuTbl(dsSV);
    }

    private void GanDuLieuTbl(ArrayList<SinhVien> dsSV) {
        DefaultTableModel model = (DefaultTableModel)tblSV.getModel();
        model.setRowCount(0);
        for(SinhVien x:dsSV){
            model.addRow( new Object[]{x.getMaSV(),x.getHoTen(),x.getGioTinh(),x.getEmail(),x.getDieThoai(),x.getDiaChi()});
        }          
        tblSV.setModel(model);
    }

    private void GanDuLieutext(SinhVien sv) {
        txtMasv.setText(sv.getMaSV());
        txtHoten.setText(sv.getHoTen());
        if(sv.getGioTinh()==true)
            rdNam.setSelected(true);
        else
            rdNu.setSelected(true);
        txtEmail.setText(sv.getEmail());
        txtSDT.setText(sv.getDieThoai());
        txtDiaChi.setText(sv.getDiaChi());
    }
